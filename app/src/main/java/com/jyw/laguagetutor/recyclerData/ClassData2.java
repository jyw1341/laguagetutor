package com.jyw.laguagetutor.recyclerData;

public class ClassData2 {
    private String mobile,image,name,age,gender,count,date;

    public ClassData2(String mobile, String image, String name, String age, String gender, String count, String date) {
        this.mobile = mobile;
        this.image = image;
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.count = count;
        this.date = date;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
