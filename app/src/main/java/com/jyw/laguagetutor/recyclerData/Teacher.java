package com.jyw.laguagetutor.recyclerData;

public class Teacher {

    private String title,name,gender,education,study_type,location,mobileNumber,image;

    public Teacher(String title, String name, String gender, String education, String study_type, String location, String mobileNumber,String image) {
        this.title = title;
        this.name = name;
        this.gender = gender;
        this.education = education;
        this.study_type = study_type;
        this.location = location;
        this.mobileNumber = mobileNumber;
        this.image = image;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getEducation() {
        return education;
    }

    public void setEducation(String education) {
        this.education = education;
    }

    public String getStudy_type() {
        return study_type;
    }

    public void setStudy_type(String study_type) {
        this.study_type = study_type;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getMobileNumber() {
        return mobileNumber;
    }

    public void setMobileNumber(String mobileNumber) {
        this.mobileNumber = mobileNumber;
    }
}
